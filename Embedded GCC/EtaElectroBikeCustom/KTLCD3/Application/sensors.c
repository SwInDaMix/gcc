static sSensors s_sensors;

sSensors const *sensors_get_current() {
	uint16_t _sens_voltage_adc = periph_get_adc_voltage();
	uint16_t _sens_temp_adc = 1023U - periph_get_adc_temperature();
	
    s_sensors.voltage = (uint16_t)(((uint32_t)_sens_voltage_adc * settings_get_current()->voltage_coefficient) >> 6);

    // calc motor temperature
    // not used due to low performance of calculation
    //_sens_temp_k = (uint8_t)roundf((1.0f / ((logf(HUD_TEMP_OPP_RESISTANCE / ((1023.0f / (float)(1023U - periph_get_adc_motor_temperature())) - 1.0f) / HUD_TEMP_NOMINAL_RESISTANCE) / HUD_TEMP_BCOEFFICIENT) + (1.0f / HUD_TEMP_NOMINAL_TEMP))) - 288.15f);
    // fast method for ktlcd3
    if(_sens_temp_adc < HUD_TEMP_LOOKUP1_START) s_sensors.system_temp = s_temp_for_ktlcd3_lookup1[0];
    else s_sensors.system_temp = s_temp_for_ktlcd3_lookup1[(_sens_temp_adc - HUD_TEMP_LOOKUP1_START) >> 1];

    return &s_sensors;
}
